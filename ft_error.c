#include "push_swap.h"

int	ft_exit()
{
	write(1, "Error\n", 7);
	exit(2);
}

void	ft_digit(char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		if ((s[i] < '0' || s[i] > '9') && (s[i] != '-' && s[i] != 32))
			ft_exit();
		if (s[i] == '-')
			if (s[i + 1] < '0' || s[i + 1] > '9')
				ft_exit();
		i++;
	}
}
